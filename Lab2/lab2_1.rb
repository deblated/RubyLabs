#тестові данні, які дадуть площу 1
x = [1,4,5]
y = [1,2,3]

size = x.length - 1
result=0.to_i

for i in 0..size do
  if(i==size)
    result += (x[i]+x[0])*(y[i]-y[0])
  else
    result += (x[i]+x[i+1])*(y[i]-y[i+1])
  end
end

result = (result.abs)/2
puts "Площа становить #{result} квадратних сантиметрів"